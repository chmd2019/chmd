function autorizar_fotos(nfamilia, idarchivo) {
  //desabilitar boton
  $('#boton_fotos').attr('disabled','disabled');
  var modal_fotos = M.Modal.getInstance($("#modal_fotos"+idarchivo));
  var estatus = '';
  if($('#autorizo').prop('checked')){
    estatus = '2';
  }
  if($('#desautorizo').prop('checked')){
    estatus = '3';
  }

  $.ajax({
    url: "https://www.chmd.edu.mx/pruebascd/icloud/misarchivos/common/post_autoriza_archivo.php",
    type: "POST",
    data: {
      submit: '1',
      nfamilia: nfamilia,
      idarchivo: idarchivo,
      estatus: estatus
    },
    success: function (res) {
      if (res == 1) {
        modal_fotos.close();
        M.toast({html: "¡Solicitud realizada con éxito!", classes: 'green accent-4'});
        setTimeout(() => {
          location.reload();
        }, 1500);
      } else {
        M.toast({html: "¡Solicitud no realizada!", classes: 'red accent-4'});
        $('#boton_fotos').removeAttr('disabled');
      }
    }
  });
}
