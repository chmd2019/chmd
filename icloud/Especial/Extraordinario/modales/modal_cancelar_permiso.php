    function cancelar_permiso(id) {
        var modal_cancelar_permiso = M.Modal.getInstance($("#modal_cancelar_permiso_"+id));
        $.ajax({
            url: "https://www.chmd.edu.mx/pruebascd/icloud/Especial/common/post_cancela_permiso.php",
